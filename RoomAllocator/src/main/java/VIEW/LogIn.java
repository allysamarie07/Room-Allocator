    private void RFIDButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RFIDButtonActionPerformed
        boolean rfidConnected = UsbRfidScanner.isRfidScannerConnected();

        if (rfidConnected) {
            JOptionPane.showMessageDialog(this, "RFID scanner detected and ready to scan.", "RFID Detected", JOptionPane.INFORMATION_MESSAGE);
            rfidField.setText("");               // Clear old data
            rfidField.requestFocusInWindow();   // Focus so scanner can type
        } else {
            JOptionPane.showMessageDialog(this, "RFID scanner not detected. Please insert an RFID scanner.", "RFID Not Detected", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_RFIDButtonActionPerformed

