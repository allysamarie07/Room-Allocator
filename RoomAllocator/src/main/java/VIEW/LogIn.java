    public LogIn(IndexView index) {
        this.index = index;
        initComponents();
        
        rfidField.addActionListener(e -> {
            String scannedRfid = rfidField.getText().trim();
            LoginViewModel.LoginResult result = viewModel.authenticateWithRFID(scannedRfid);

            if (result.success) {
                JOptionPane.showMessageDialog(this, "Welcome " + result.accountName + " (" + result.accountType + ")!");
                // Proceed to appropriate dashboard here...
                openIndex(result);
            } else {
                JOptionPane.showMessageDialog(this, result.message, "RFID Login Failed", JOptionPane.ERROR_MESSAGE);
                rfidField.setText("");
            }
        });
        
        this.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosed(java.awt.event.WindowEvent e) {
                if (index != null) {
                    index.logInInstance = null;
                }
            }
        });
    }
    
    public void openIndex(LoginViewModel.LoginResult result){
        switch (result.accountType) {
            case "Dean":
                JOptionPane.showMessageDialog(this, "Login Successful as Dean!", "Success", JOptionPane.INFORMATION_MESSAGE);
                new IndexView(result.accountName, result.accountType).setVisible(true);
                break;

            case "Faculty":
                JOptionPane.showMessageDialog(this, "Login Successful as Faculty!", "Success", JOptionPane.INFORMATION_MESSAGE);
                new IndexView(result.accountName, result.accountType).setVisible(true);
                break;

            default:
                JOptionPane.showMessageDialog(this, "Invalid AccountType. Please contact the dean's office.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
        }
        dispose();
        index.dispose();
    }


