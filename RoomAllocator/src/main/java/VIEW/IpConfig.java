        /**
     * Loads configuration from IPCONFIG.txt file and sets text fields.
     */
    private void loadConfigFromViewModel() {
        if (!viewModel.loadConfig()) {
            JOptionPane.showMessageDialog(this, "Config not found!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            IPaddress.setText(viewModel.getIp());
            IPport.setText(viewModel.getPort());
            DBname.setText(viewModel.getDbName());
            DBusername.setText(viewModel.getUsername());
            DBpassword.setText(viewModel.getPassword());
        }
    }
