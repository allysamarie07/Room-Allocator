    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        boolean success = viewModel.saveConfig(
        IPaddress.getText(),
        IPport.getText(),
        DBname.getText(),
        DBusername.getText(),
        new String(DBpassword.getPassword())
        );

        if (success) {
            JOptionPane.showMessageDialog(this, "Config saved successfully.");
        } else {
            JOptionPane.showMessageDialog(this, "Failed to save config.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_SaveButtonActionPerformed

